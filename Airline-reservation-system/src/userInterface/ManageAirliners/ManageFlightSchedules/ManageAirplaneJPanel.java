    public void populateJTable(){
        DefaultTableModel defaultTM = (DefaultTableModel) jTblAirplanes.getModel();
        defaultTM.setRowCount(0);
        for (Airplane airplane : airliner.getAirplaneCatalog()){
            Object row[] = new Object[jTblAirplanes.getColumnCount()];
            row[0] = airplane;
            row[1] = airplane.getModelNum();
            row[2] = airplane.getSerialNum();
            row[3] = airplane.getPassengerCapacity();
            defaultTM.addRow(row);
        }        
    }
