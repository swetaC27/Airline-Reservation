    private void jBtnViewPlaneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnViewPlaneActionPerformed
        // TODO add your handling code here:
        Airplane selectedAirplane;
        int selectedRow =  jTblAirplanes.getSelectedRow();

        if(selectedRow == -1){
            JOptionPane.showMessageDialog(null, "Please select an airliner");
        }
        else{
            selectedAirplane = (Airplane) jTblAirplanes.getValueAt(jTblAirplanes.getSelectedRow(), 0);
            ViewAirplaneJPanel viewAirplanePanel = new ViewAirplaneJPanel(CardSequenceJPanel, travelAgency, airliner, selectedAirplane);
            CardSequenceJPanel.add("ViewAirplaneJPanel", viewAirplanePanel);
            layout.next(CardSequenceJPanel);

        }

    }//GEN-LAST:event_jBtnViewPlaneActionPerformed

    private void jBtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnDeleteActionPerformed
        // TODO add your handling code here:
       Airplane selectedAirplane;
        int selectedRow =  jTblAirplanes.getSelectedRow();

       if(selectedRow == -1){
            JOptionPane.showMessageDialog(null, "Please select an airplane");
       }
        else{
           selectedAirplane = (Airplane) jTblAirplanes.getValueAt(jTblAirplanes.getSelectedRow(), 0);
           airliner.deleteAirplane(selectedAirplane);
           populateJTable();
        }
    }//GEN-LAST:event_jBtnDeleteActionPerformed

